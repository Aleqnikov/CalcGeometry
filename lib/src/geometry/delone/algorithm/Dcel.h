#ifndef DCEL_H
#define DCEL_H

/**
 * @brief Doubly-Connected Edge List (DCEL) — структура данных для хранения
 * планарного подразбиения. Используется для триангуляции Делоне.
 *
 * Каждое полуребро (half-edge) хранит:
 *   - индекс вершины-источника
 *   - индекс грани (face)
 *   - twin, prev, next — индексы смежных полурёбер
 *
 * Индекс 0 зарезервирован как "нулевой" (нет объекта).
 * Бесконечная вершина имеет индекс INFINITE_VERTEX.
 */

#include <vector>
#include <cstddef>
#include <cstdint>
#include "Point2D.h"

static const size_t INVALID = SIZE_MAX;

class DCEL {
public:
    // -----------------------------------------------------------------------
    // Внутренние записи
    // -----------------------------------------------------------------------
    struct Vertex {
        Point2D pos;
        size_t  edge = INVALID; // одно из исходящих полурёбер
        bool    infinite = false;
    };

    struct HalfEdge {
        size_t origin = INVALID; // индекс вершины-источника
        size_t twin   = INVALID;
        size_t face   = INVALID;
        size_t prev   = INVALID;
        size_t next   = INVALID;
        size_t node   = INVALID; // узел DelaunayTree (для алгоритма)
    };

    struct Face {
        size_t edge = INVALID;   // одно из инцидентных полурёбер
        size_t node = INVALID;   // узел DelaunayTree
        bool   infinite = false;
    };

    // -----------------------------------------------------------------------
    // Индекс "бесконечной" вершины
    // -----------------------------------------------------------------------
    size_t infinite_vertex = INVALID;

    // -----------------------------------------------------------------------
    // Хранилища
    // -----------------------------------------------------------------------
    std::vector<Vertex>   vertices;
    std::vector<HalfEdge> edges;
    std::vector<Face>     faces;

    // -----------------------------------------------------------------------
    // Создание объектов
    // -----------------------------------------------------------------------
    size_t new_vertex(Point2D pos, bool infinite = false);
    size_t new_edge();   // создаёт пару twin-полурёбер; возвращает индекс первого
    size_t new_face(bool infinite = false);

    // -----------------------------------------------------------------------
    // Вспомогательные запросы
    // -----------------------------------------------------------------------
    bool is_infinite_vertex(size_t v) const;
    bool is_infinite_edge(size_t e)   const;
    bool is_infinite_face(size_t f)   const;

    // Вершины грани (против часовой стрелки)
    void face_vertices(size_t f, size_t& v0, size_t& v1, size_t& v2) const;

    // -----------------------------------------------------------------------
    // Операции изменения
    // -----------------------------------------------------------------------

    /**
     * Разбить грань f на три, вставив точку p.
     * Предполагается, что f — конечный треугольник.
     * Возвращает индекс новой вершины.
     */
    size_t split_face(size_t f, Point2D p);

    /**
     * Повернуть ребро e (flip).
     * e и его twin должны быть рёбрами двух смежных треугольников.
     * Возвращает новый индекс ребра e (после flip).
     */
    size_t flip_edge(size_t e);

    // -----------------------------------------------------------------------
    // Инициализация — добавляем "суперструктуру" с бесконечной вершиной
    // -----------------------------------------------------------------------
    /**
     * Создать начальную структуру:
     *   - бесконечная вершина v_inf
     *   - три конечных вершины a, b, c (большой треугольник, содержащий все точки)
     *   - один конечный треугольник abc
     *   - три "бесконечных" треугольника, примыкающих к рёбрам abc снаружи
     * Возвращает индекс конечного треугольника.
     */
    size_t init_super_triangle(Point2D a, Point2D b, Point2D c);
};

#endif // DCEL_H